# Write a function sum_product(a,b) that returns both the sum and the product
# of two numbers, the result should be returned as a tuple (sum, product).
# #

def sum_product(a,b):
    a = (a + b)
    p = (a * b)
    return a, p

print(sum_product(5,30))

